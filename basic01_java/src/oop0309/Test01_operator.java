package oop0309;

public class Test01_operator {

	public static void main(String[] args) {
		System.out.println(5+3);
		System.out.println(5-3);
		System.out.println(5*3);
		System.out.println(5/3);
		
		System.out.println(5<3);
		System.out.println(5>3);
		System.out.println(5==3);
		System.out.println(5!=3);
		System.out.println(5<=3);
		System.out.println(5<=3);
		
		System.out.println(5<3 && 2<4);
		System.out.println(5>3 || 2<4);
		

		
	}

}
